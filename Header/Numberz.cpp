//Addition
double NumberAdd(double A, double B) {

	//How the sum is found
	double sum = A + B;

	return sum;
}

//Division
double NumberDivide(double a, double b) {

	//How the quotient is found
	double quotient = a / b;

	return quotient;

}


//Multiplication
double NumberMultiply(double a, double b) {

	//How the product is found
	double product = a * b;

	return product;
}
